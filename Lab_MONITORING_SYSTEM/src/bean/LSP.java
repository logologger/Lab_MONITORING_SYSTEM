package bean;

public class LSP {
private String lsp_name;
private String lsp_email;
private String lsp_contact;
private String lsp_address;
public String getLsp_name() {
	return lsp_name;
}
public void setLsp_name(String lsp_name) {
	this.lsp_name = lsp_name;
}
public String getLsp_email() {
	return lsp_email;
}
public void setLsp_email(String lsp_email) {
	this.lsp_email = lsp_email;
}
public String getLsp_contact() {
	return lsp_contact;
}
public void setLsp_contact(String lsp_contact) {
	this.lsp_contact = lsp_contact;
}
public String getLsp_address() {
	return lsp_address;
}
public void setLsp_address(String lsp_address) {
	this.lsp_address = lsp_address;
}
	
	
}
