package bean;
public class Patient {
	private String patientID, firstName, lastName, gender, address, age, contactNo,medicalComplaint,referDoctor;
	String healthInsuranceNo;
	private String HCP_ID;
	private String bill;
	public String getPatientID() {
		return patientID;
	}
	public void setPatientID(String patientID) {
		this.patientID = patientID;
	}
	public String getFirstName() {
		return firstName;
	}
	public void setFirstName(String firstName) {
		this.firstName = firstName;
	}
	public String getLastName() {
		return lastName;
	}
	public void setLastName(String lastName) {
		this.lastName = lastName;
	}
	public String getGender() {
		return gender;
	}
	public void setGender(String gender) {
		this.gender = gender;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	public String getAge() {
		return age;
	}
	public void setAge(String age) {
		this.age = age;
	}
	public String getContactNo() {
		return contactNo;
	}
	public void setContactNo(String contactNo) {
		this.contactNo = contactNo;
	}
	public String getMedicalComplaint() {
		return medicalComplaint;
	}
	public void setMedicalComplaint(String medicalComplaint) {
		this.medicalComplaint = medicalComplaint;
	}
	public String getReferDoctor() {
		return referDoctor;
	}
	public void setReferDoctor(String referDoctor) {
		this.referDoctor = referDoctor;
	}
	public String getHealthInsuranceNo() {
		return healthInsuranceNo;
	}
	public void setHealthInsuranceNo(String healthInsuranceNo) {
		this.healthInsuranceNo = healthInsuranceNo;
	}
	public String getHCP_ID() {
		return HCP_ID;
	}
	public void setHCP_ID(String hCP_ID) {
		HCP_ID = hCP_ID;
	}
	public String getBill() {
		return bill;
	}
	public void setBill(String bill) {
		this.bill = bill;
	}
}
